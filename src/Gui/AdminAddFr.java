    public boolean validateEndDate(JDateChooser dateChooser) {
        Date selectedDate = dateChooser.getDate();

        Date currentDate = new Date();
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(currentDate);
        calendar.set(Calendar.HOUR_OF_DAY, 0);
        calendar.set(Calendar.MINUTE, 0);
        calendar.set(Calendar.SECOND, 0);
        calendar.set(Calendar.MILLISECOND, 0);
        currentDate = calendar.getTime();

        // Compare the selected date with the current date
        if (selectedDate.before(currentDate)) {
            JOptionPane.showMessageDialog(this, "END DATE CANNOT BE BEFORE CURRENT DATE", "Invalid Date", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    
    }
    
     public boolean validateStartDate(JDateChooser dateChooser) {
        Date selectedDate = dateChooser.getDate();

        Date currentDate = new Date();
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(currentDate);
        calendar.set(Calendar.HOUR_OF_DAY, 0);
        calendar.set(Calendar.MINUTE, 0);
        calendar.set(Calendar.SECOND, 0);
        calendar.set(Calendar.MILLISECOND, 0);
        currentDate = calendar.getTime();

        // Compare the selected date with the current date
        if (selectedDate.before(currentDate) || selectedDate.after(currentDate)) {
            JOptionPane.showMessageDialog(this, "SELECT START DATE AS THE CURRENT DATE", "Invalid Date", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    
    }
    
