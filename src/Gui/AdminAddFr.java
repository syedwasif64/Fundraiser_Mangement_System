    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (checkFields()) {
                String name=txtName.getText();
                String des=txtDescription.getText();
                String startDate=dateFormat(dateS.getDate());
                Boolean validationStartDate=validateStartDate(dateS);
                if (!validationStartDate) {
                   setFieldsEmpty();
                    return;
                }
                Boolean validationEndDate=validateEndDate(dateE);
                if (!validationEndDate) {
                    setFieldsEmpty();
                    return;
                }
                String endDate=dateFormat(dateE.getDate());
                double amount=Double.parseDouble(txtAmount.getText());
                Fundraiser fr=new Fundraiser(name, des, startDate,endDate, amount, 0.0);
                fm.addFundRaiser(fr);
                setFieldsEmpty();
                DataBase db=new DataBase();
                String result=db.addFRtoDB(name, startDate, endDate, des, amount);
                JOptionPane.showMessageDialog(this, result);
        }
        else{
            JOptionPane.showMessageDialog(this, "FIELDS EMPTY");
            return;
            }
      
        
    }//GEN-LAST:event_btnUpdateActionPerformed

