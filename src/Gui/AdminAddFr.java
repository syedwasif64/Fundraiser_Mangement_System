    private String dateFormat(Date date){
        try {
            SimpleDateFormat dtFormat=new SimpleDateFormat("dd/MM/yyyy");
            return dtFormat.format(date);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: "+e);
            return "";
        }
    }
